/*
 * File: SwarmConstancts.java
 * ------------------------------
 * This file declares several constants that are shared by the
 * different modules in the Swarm application.  Any class
 * that implements this interface can use these constants.
 */

public interface SwarmConstants {
	
/** The width of the application window */
	public static final int APPLICATION_WIDTH = 400;

/** The height of the application window */
	public static final int APPLICATION_HEIGHT = 600;

/** The length in pixels of the side of the square that follows the mouse */
	public static final int MOUSERECT_LENGTH=20;	
	
/** The number of high scores to be displayed*/
	public static final int HIGH_SCORES=3;
}
